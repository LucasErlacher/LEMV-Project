
export default httpClient => ({
  getNews: async () => {
    const response = await httpClient.get('/news')
    let errors = null

    if (!response.data) {
      errors = {
        status: response.request.status,
        statusText: response.request.statusText
      }
    }

    return {
      data: response.data,
      errors
    }
  }
})
